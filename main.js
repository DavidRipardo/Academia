        var alturaMetros = altura / 100;
        var imc = (peso / (alturaMetros * alturaMetros)).toFixed(4);
        var imcQuatroDigitos = imc.slice(0, 4);
        var resultado = "Seu IMC Ã© " + imcQuatroDigitos;

